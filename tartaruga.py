import turtle


turtle.title('Brincando com python e tentando entender')
turtle.bgcolor('light salmon')
tartaruga = turtle.Turtle()
tartaruga.shapesize(2,2,5)
tartaruga.fillcolor("light pink")
tartaruga.pencolor("#DA70D6")
tartaruga.speed(1)
tartaruga.shape("turtle")
tartaruga.begin_fill()
tartaruga.pensize(3)
tartaruga.left(50)
tartaruga.forward(133)
tartaruga.circle(50,200)
tartaruga.right(140)
tartaruga.circle(50,200)
tartaruga.forward(133)
tartaruga.end_fill
